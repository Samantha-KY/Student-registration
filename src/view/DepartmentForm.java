    public void populate_table(){
        ResultSet r = controller.listDepartment();
        tb_list.setModel(DbUtils.resultSetToTableModel(r));
    }
    public void clear_txt_fields(){
        txt_id.setText("");
        txt_name.setText("");
        txt_head.setText("");
    }
    
    
    
    
    private void btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveActionPerformed
        
        Department d = new Department();
        d.setDeptID(txt_id.getText());
        d.setDeptName(txt_name.getText());
        d.setDeptHead(txt_head.getText());
        controller.createDepartment(d);
        populate_table();
        clear_txt_fields();
    }//GEN-LAST:event_btn_saveActionPerformed

    private void tb_listMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_listMouseClicked
   
        try{
            int row = tb_list.getSelectedRow();
            String deptId = (tb_list.getModel().getValueAt(row,0).toString());
            ResultSet rd = controller.searchById(deptId);
            if(rd.next()){
                txt_id.setText(rd.getString("id"));
                txt_name.setText(rd.getString("deptname"));
                txt_head.setText(rd.getString("depthead"));
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
         }finally{
            connector.disconnect();
        }
    }//GEN-LAST:event_tb_listMouseClicked

