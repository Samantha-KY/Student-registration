    private void tb_listMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_listMouseClicked
        // TODO add your handling code here:
        try{
            int selectedRow = tb_list.getSelectedRow();
            String studentId = (tb_list.getModel().getValueAt(selectedRow, 0).toString());
            ResultSet ra = studentController.searchStudent(studentId);
            if(ra.next()){
                txt_sid.setText(ra.getString("stId"));
                txt_sname.setText(ra.getString("stName"));
                cb_dept.setSelectedItem(ra.getString("deptId"));
                txt_dob.setText(ra.getDate("dob").toString());
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
    }
    }//GEN-LAST:event_tb_listMouseClicked

