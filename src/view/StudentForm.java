    private void btn_searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_searchActionPerformed
    try {
        // TODO add your handling code here:
        ResultSet a = studentController.searchStudent(txt_search.getText());
        if(!a.next()){
            throw new StudentNotFoundException("Student does not exist");
        }else{
            txt_sid.setText(a.getString("stId"));
            txt_sname.setText(a.getString("stName"));
            cb_dept.setSelectedItem(a.getString("deptId"));
            txt_dob.setText(a.getDate("dob").toString());
        }
    }catch(StudentNotFoundException e){
        JOptionPane.showMessageDialog(null, "Student does not exist");
    }catch(SQLException e){
        e.printStackTrace();
    }
    
    }//GEN-LAST:event_btn_searchActionPerformed

